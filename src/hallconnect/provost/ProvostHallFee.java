    private void btn_accept1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_accept1ActionPerformed
        // TODO add your handling code here:
        if (student_name == null || session == null || semestr == null) {
            JOptionPane.showMessageDialog(this, "Please Select any row to accept");
        } else {

            try {
                Connection con = DbConnection.getConnection();
                PreparedStatement pst = con.prepareStatement("INSERT INTO payment (username,session,semester,payment_status) values(?,?,?,?)");
                pst.setString(1, student_name);
                pst.setString(2, session);
                pst.setString(3, semestr);
                pst.setString(4, "Paid");
                int row = pst.executeUpdate();
                if (row > 0) {
                    delete();
                    JOptionPane.showMessageDialog(this, "Accepted Successfully");
                    showRechord(provost_username);
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        }

    }//GEN-LAST:event_btn_accept1ActionPerformed

