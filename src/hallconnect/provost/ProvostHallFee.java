    private void btn_acceptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_acceptActionPerformed
        // TODO add your handling code here:
        try {
            // Get selected year as an integer
            int selectedYear = date_year.getYear();
            // Convert to string
            String session = String.valueOf(selectedYear);
            String semester = combo_semester.getSelectedItem().toString();
            String amount = txt_amount.getText();
            Connection con = DbConnection.getConnection();
            PreparedStatement pst = con.prepareStatement("INSERT INTO semester (session,semester,fee) values (?,?,?)");
            pst.setString(1, session);
            pst.setString(2, semester);
            pst.setString(3, amount);
            int rowCount = pst.executeUpdate();
            if (rowCount > 0) {
                JOptionPane.showMessageDialog(this, "Semester Fee Added Successfully");
                txt_amount.setText("");
                combo_semester.setSelectedItem("0");

            }
        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_btn_acceptActionPerformed

