    void showRechord(String provost_username) {
        DefaultTableModel model = (DefaultTableModel) table_feeRequest.getModel();

        hall_name = switch (provost_username) {
            case "abu_naser" ->
                "BANGABONDHU HALL";
            case "naznin_ara" ->
                "BANGAMATA HALL";
            default ->
                "MUKTIJODDHA HALL";
        };
        try {
            Connection con = DbConnection.getConnection();
            PreparedStatement pst = con.prepareStatement("SELECT * FROM pending_payment WHERE hallName=?");
            pst.setString(1, hall_name);
            ResultSet rs = pst.executeQuery();
            model.setRowCount(0);
            while (rs.next()) {
                String username = rs.getString("username");
                String session = rs.getString("session");
                String semester = rs.getString("semester");
                String method = rs.getString("method");
                String transaction_id = rs.getString("transaction_id");
                Object obj[] = {username, session, semester, method, transaction_id};
                model.addRow(obj);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }

    boolean delete() {
        try {
            Connection con = DbConnection.getConnection();
            PreparedStatement pst = con.prepareStatement("DELETE from pending_payment where username = ? AND semester=?");
            pst.setString(1, student_name);
            pst.setString(2, semestr);
            int row = pst.executeUpdate();
            return row > 0;
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        return false;
    }

